window.onload = function() {
    $("#submitContactButton").prop("disabled", true);
};

$("#ContactName, #ContactEmail, #ContactText").keyup(function() {
	checkFormSubmit();
});

function checkFormSubmit() {
	if ($("#ContactName").val()&&$("#ContactEmail").val()&&validEmail($("#ContactEmail").val())&&$("#ContactText").val()) {
        $("#submitContactButton").prop("disabled", false);
    }
    else {
        $("#submitContactButton").prop("disabled", true);
    }
}

